package com.xatu.file.bean;

/**
 * 学生文件
 * 
 * @author zpp
 * 
 */
public class StudentFile {

	private int studentFile_ID;
	private String studentFile_fileName;
	private String studentFile_browseTime;
	private int studentFile_download;
	private String studentFile_studentName;
	private int studentFile_uploading;
	private String studentFile_evaluate;

	public int getStudentFile_ID() {
		return studentFile_ID;
	}

	public void setStudentFile_ID(int studentFile_ID) {
		this.studentFile_ID = studentFile_ID;
	}

	public String getStudentFile_fileName() {
		return studentFile_fileName;
	}

	public void setStudentFile_fileName(String studentFile_fileName) {
		this.studentFile_fileName = studentFile_fileName;
	}

	public String getStudentFile_browseTime() {
		return studentFile_browseTime;
	}

	public void setStudentFile_browseTime(String studentFile_browseTime) {
		this.studentFile_browseTime = studentFile_browseTime;
	}

	public int getStudentFile_download() {
		return studentFile_download;
	}

	public void setStudentFile_download(int studentFile_download) {
		this.studentFile_download = studentFile_download;
	}

	public String getStudentFile_studentName() {
		return studentFile_studentName;
	}

	public void setStudentFile_studentName(String studentFile_studentName) {
		this.studentFile_studentName = studentFile_studentName;
	}

	public int getStudentFile_uploading() {
		return studentFile_uploading;
	}

	public void setStudentFile_uploading(int studentFile_uploading) {
		this.studentFile_uploading = studentFile_uploading;
	}

	public String getStudentFile_evaluate() {
		return studentFile_evaluate;
	}

	public void setStudentFile_evaluate(String studentFile_evaluate) {
		this.studentFile_evaluate = studentFile_evaluate;
	}

}
